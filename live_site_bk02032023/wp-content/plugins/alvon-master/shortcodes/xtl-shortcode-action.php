if(function_exists( 'Vc_Manager' ) ) {

    /* Visual composer addons custom fonts
    =====================================================*/
    function alvon_add_new_icon_set_to_iconbox( ) {
        $param = WPBMap::getParam( 'vc_icon', 'type' );
        $param['value'][__( 'Flaticons', 'total' )] = 'fontawesomepro';
        vc_update_shortcode_param( 'vc_icon', $param );
    }
    add_filter( 'init', 'alvon_add_new_icon_set_to_iconbox', 40 );
    // Add font picker setting to icon box module when you select your font family from the dropdown
    function alvon_add_font_picker() {
      vc_add_param( 'vc_icon', array(
          'type' => 'iconpicker',
          'heading' => esc_html__( 'Icon', 'total' ),
          'param_name' => 'icon_fontawesomepro',
          'settings' => array(
            'emptyIcon' => true,
            'type' => 'fontawesomepro',
            'iconsPerPage' => 2314,
          ),
        )
      );
    }
    add_filter( 'vc_after_init', 'alvon_add_font_picker', 40 );

}
